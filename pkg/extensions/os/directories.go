package os

import (
	"fmt"
	"io"
	"io/ioutil"
	"os"
	"path"
)

func Mkdir(dir string) error {
    err := os.MkdirAll(dir, os.ModePerm)
    if err != nil {
        return err
    }
    return nil
}

func MkdirInArray(keys []string) error {
    for _, dir := range keys {
        err := Mkdir(dir)
        if err != nil {
            return err
        }
    }
    return nil
}

func MkdirInMap(items map[string]string, keys []string) error {
    for _, k := range keys {
        dir := items[k]
        if len(dir) == 0 {
            continue
        }
        err := Mkdir(dir)
        if err != nil {
            return err
        }
    }
    return nil
}

func CopyDir(src string, dst string) error {
	var err error
	var fds []os.FileInfo
	var srcinfo os.FileInfo

	if srcinfo, err = os.Stat(src); err != nil {
		return err
	}

	if err = os.MkdirAll(dst, srcinfo.Mode()); err != nil {
		return err
	}

	if fds, err = ioutil.ReadDir(src); err != nil {
		return err
	}
	for _, fd := range fds {
		srcfp := path.Join(src, fd.Name())
		dstfp := path.Join(dst, fd.Name())

		if fd.IsDir() {
			if err = CopyDir(srcfp, dstfp); err != nil {
				fmt.Println(err)
			}
		} else {
			if err = CopyFile(srcfp, dstfp); err != nil {
				fmt.Println(err)
			}
		}
	}
	return nil
}

func CopyFile(src, dst string) error {
	var err error
	var srcfd *os.File
	var dstfd *os.File
	var srcinfo os.FileInfo

	if srcfd, err = os.Open(src); err != nil {
		return err
	}
	defer srcfd.Close()

	if dstfd, err = os.Create(dst); err != nil {
		return err
	}
	defer dstfd.Close()

	if _, err = io.Copy(dstfd, srcfd); err != nil {
		return err
	}
	if srcinfo, err = os.Stat(src); err != nil {
		return err
	}
	return os.Chmod(dst, srcinfo.Mode())
}
